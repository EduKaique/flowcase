
class User {
  final String id;
  final String username;
  final String email;
  final String name;
  final String age;
  final String role;
  final bool active;
  final DateTime created;

  User({
    required this.id,
    required this.username,
    required this.email,
    required this.name,
    required this.age,
    required this.role,
    required this.active,
    required this.created,
  });

  
}
